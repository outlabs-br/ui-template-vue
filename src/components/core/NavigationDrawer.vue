          </v-list-tile-action>
          <v-list-tile-title :class="[{'active': selectedIndex === 6}, 'item-title' ]">{{ $t('media') }}</v-list-tile-title>
        </v-list-tile>
    </v-list-group>



    </v-list>
  </v-navigation-drawer>
